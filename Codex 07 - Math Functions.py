import math

pi = -3.14
x = 1
y = 2
z = 3

print(round(pi))       # Round a number
print(math.ceil(pi))   # Round up a number
print(math.floor(pi))  # Round down a number
print(math.sqrt(420))  # Square root of a number
print(abs(pi))         # Absolute value of a number
print(pow(pi, 2))      # Exponent of a number
print(max(x,y,z))      # Maximum out of given variables
print(min(x,y,z))      # Minimum out of given variables
